package spring.beans;

/**
 * @author linlang
 * @date 2018/12/4
 */
public class Person {
    private  String name;
    private  String email;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }
}
